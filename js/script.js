document.addEventListener('DOMContentLoaded', function () {
    // 다국어 지원 기능
    const langBtns = document.querySelectorAll('.lang-btn');
    const translatableElements = document.querySelectorAll('[data-i18n]');
    const translatableContentElements = document.querySelectorAll('[data-i18n-content]');

    function updateLanguage(lang) {
        // HTML lang 속성 및 body data-lang 속성 업데이트
        document.documentElement.lang = lang;
        document.body.setAttribute('data-lang', lang);

        // 텍스트 내용 업데이트
        translatableElements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        // meta content 등 속성 업데이트
        translatableContentElements.forEach(el => {
            const key = el.getAttribute('data-i18n-content');
            if (translations[lang] && translations[lang][key]) {
                el.setAttribute('content', translations[lang][key]);
            }
        });

        // 버튼 활성 상태 업데이트
        langBtns.forEach(btn => {
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });

        // 로컬 스토리지에 설정 저장
        localStorage.setItem('preferred-lang', lang);
    }

    // 버튼 클릭 이벤트 바인딩
    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            updateLanguage(lang);
        });
    });

    // 초기 언어 설정 (로컬 스토리지 또는 브라우저 언어)
    const savedLang = localStorage.getItem('preferred-lang');
    const browserLang = navigator.language.slice(0, 2);
    const initialLang = savedLang || (translations[browserLang] ? browserLang : 'ko');

    updateLanguage(initialLang);

    // FAQ 토글 기능
    const faqItems = document.querySelectorAll('.faq-item');

    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        const answer = item.querySelector('.faq-answer');

        if (answer) {
            // 초기에는 답변을 숨김
            answer.style.maxHeight = '0';
            answer.style.overflow = 'hidden';
            answer.style.transition = 'max-height 0.3s ease';

            question.addEventListener('click', () => {
                const isActive = item.classList.contains('active');

                // 모든 FAQ 항목을 닫음
                faqItems.forEach(otherItem => {
                    otherItem.classList.remove('active');
                    const otherAnswer = otherItem.querySelector('.faq-answer');
                    if (otherAnswer) otherAnswer.style.maxHeight = '0';
                });

                // 클릭한 항목이 이미 활성화되어 있지 않으면 활성화
                if (!isActive) {
                    item.classList.add('active');
                    answer.style.maxHeight = answer.scrollHeight + 'px';
                }
            });
        }
    });

    // 스크롤 애니메이션 효과
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('animate');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    // 관찰할 요소들
    const animateElements = [
        ...document.querySelectorAll('.section-title'),
        ...document.querySelectorAll('.ai-foundry-grid'),
        ...document.querySelectorAll('.value-card'),
        ...document.querySelectorAll('.service-card'),
        ...document.querySelectorAll('.tech-segment'),
        ...document.querySelectorAll('.process-step'),
        ...document.querySelectorAll('.solution-card'),
        ...document.querySelectorAll('.faq-item'),
        ...document.querySelectorAll('.agent-card'),
        ...document.querySelectorAll('.adv-card')
    ];

    // 관찰 시작
    animateElements.forEach(el => {
        el.classList.add('fade-in');
        observer.observe(el);
    });

    if (window.location.hash) {
        history.replaceState('', document.title, window.location.pathname + window.location.search);
    }
});

